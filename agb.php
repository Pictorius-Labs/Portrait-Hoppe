	<p>
	  <h1>Allgemeine Geschäftsbedingungen</h1>
	  <br />

	  <b>Für Portraits</b> benötige ich Vorlagen aus verschiedenen Perspektiven, um einen räumlichen Eindruck zu gewinnen.
	  <br />
	  Diese können vom Auftraggeber eingereicht oder vom Auftragnehmer angefertigt werden. Soll das Portrait Persönlichkeit ausstrahlen, sind darüber hinaus weitere Informationen über die darzustellende Person (Charaktereigenschaften, Hobbies, Interessen etc.) wichtig.
	  <br />
	  Die Wahl von Größe, Material und Ausarbeitung geschieht in Absprache mit dem Auftraggeber. Daraufhin erfolgt eine detaillierte Angebotsbeschreibung mit Kostenvoranschlag.
	  Wird das Angebot angenommen und der Auftrag erteilt, ist ein erster Abschlag in Höhe eines Drittels der Auftragssumme zu zahlen. Bei Portraits wird vor der Ausarbeitung in Stein ein Modell erstellt.
	  <br />
	  Der Auftraggeber nimmt das Modell unter Absprache in einem gemeinsam festgesetzten Zeitraum ab und zahlt vor der weiteren Umsetzung in Stein ein zweites Drittel der Auftragssumme. Nach Fertigstellung, Montage und zeitnaher Abnahme der Arbeit ist innerhalb von 14 Tagen das letzte Drittel der Zahlung fällig.
	  <br />
	  <br />
	</p>

	<p>
	  <b>Bei Bauschmuck und Gartenskulpturen nach Auftrag</b> können ebenfalls Entwurfsvorlagen, Fotos, Zeichnungen oder Modelle als Arbeitsgundlage herangezogen werden. Nach einem Briefing des Auftrags erfolgt ein detailliertes Angebot.
	  <br /> 
	  Wird das Angebot angenommen und der Auftrag erteilt, ist ein erster Abschlag in Höhe eines Drittels der Auftragssumme zu zahlen.
	  Je nach Abspache und Komplexität des Auftrags wird der Bauschmuck/die Skulptur ebenfalls als Modell vorgefertigt oder frei gehauen. Im Falle einer Modellerstellung wird diese im Angebot als Posten berücksichtigt und nach Absprache in einem angemessenen Zeitraum vom Auftraggeber abgenommen. Nach der Abnahme ist die Zahlung des zweiten Drittels innerhalb von 14 Tagen fällig.
	  <br />
	  Die Zahlung des dritten Drittels der Auftragssumme erfolgt innerhalb von 14 Tagen nach Fertigstellung bzw. Montage der Arbeit und zeitnaher, vereinbarter Abnahme.
	  <br />
	  <br />
	</p>

	<p>
	  <b>Freie, ohne Auftrag erstellte Arbeiten</b> werden zum vereinbarten Preis ab Standort bar bezahlt (z.B. bei Käufen auf Ausstellungen mit Eigentransport des Käufers).
	  Ansonsten wird für das Objekt, Transport und Montage ein Angebot erarbeitet, das bei Bestellung mit zwei Dritteln bezahlt wird.
	  <br />
	  Nach  Aufstellung und Abnahme wird innerhalb von 14 Tagen  das letzte ausstehende Drittel der vereinbarten Summe als Zahlung fällig.
	  <br />
	  <br />
	</p>
